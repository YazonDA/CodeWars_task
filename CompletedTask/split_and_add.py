def split_and_add(numbers, n):
	while n and len(numbers) - 1:
		n -= 1
		ln = len(numbers)
		z = ln % 2 
		if z: numbers.insert(0, 0)
		d = (ln + z) // 2
		numbers = [numbers[i] + numbers[i + d] for i in range(d)]
	return numbers

